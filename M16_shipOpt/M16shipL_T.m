%模型实验数据
L_T = [0.878977428	2.55018609
0.892156534	2.560205816
0.905683112	2.570228155
0.918829561	2.581117866
0.933051082	2.59114543
0.943882794	2.598884905
0.955035853	2.607322982
0.964851276	2.614358631
0.975656863	2.622794095
0.986822984	2.630884177
0.998998863	2.639852083
];

%吃水船长曲线拟合
L = L_T(:,2);
T = L_T(:,1);
p = polyfit(T,L,2);
