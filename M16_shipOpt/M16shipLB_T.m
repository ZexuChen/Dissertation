function shipLB = M16shipLB_T(t)
%计算某吃水比t下的船长船宽比L/B

%模型实验数据
L_B1 = [0.880573409	10.9571928
0.900531904	11.02015393
0.929778575	11.11365724
0.950083614	11.17665162
0.970372534	11.2466212

0.989299458	11.305995
];

LB1 = L_B1(:,2);
T1 = L_B1(:,1);
p1 = polyfit(T1,LB1,2);
if t<1 
   shipLB = polyval(p1,t);
else 
    shipLB = 11.34;
end

